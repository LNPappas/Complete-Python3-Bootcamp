#Comparison Operators
print("a == b <- if both are equal return True else False")
print("'2' == 2 <- False")
print("2.0 == 2 <- True")
print("a != b <- if both are not equal return True else False")
print("a > b <- if a greater than b return True else False")
print("a < b <- if a less than b return True else False")
print("a >= b <- if a greater then or equal to b return True else False")
print("a <= b <- if a less than or equal to b return True else False")

#Logical Operators
print("and: 1 < 2 and 2 < 3 <- True (both need to be true) == 1 < 2 < 3")
print("or: 1 == 1 or 2 == 2 <- True (only one needs to be true)")
print("not: not 1 == 1 <- returns False (the opposite boolean) == 1!=1 in this case")




